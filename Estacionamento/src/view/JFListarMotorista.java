    private void jBtnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAlterarActionPerformed
        if(jTMotorista.getSelectedRow() != -1){
            int MotoristaSelecionada = (int)jTMotorista.getValueAt(jTMotorista.getSelectedRow(), 0);
            JFAtualiazarMotorista am = new JFAtualiazarMotorista(MotoristaSelecionada);
            am.setVisible(true);
        }else{
            JOptionPane.showMessageDialog(null, "Selecione uma Motorista!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
        readJTable();
    }//GEN-LAST:event_jBtnAlterarActionPerformed

    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
        // TODO add your handling code here:
        if(jTMotorista.getSelectedRow() != -1){
            int opcao = JOptionPane.showConfirmDialog(null, "Deseja Excluir a vaga selecionada?", "Exclus√£o", JOptionPane.YES_NO_OPTION);
            if(opcao == 0){
                MotoristaDAO dao = new MotoristaDAO();
                Motorista v = new Motorista();

                v.setidMotorista((int)jTMotorista.getValueAt(jTMotorista.getSelectedRow(), 0));
                dao.delete(v);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Selecione um Motorista!", "Error", JOptionPane.ERROR_MESSAGE);
        }

        readJTable();
    }//GEN-LAST:event_jBtnExcluirActionPerformed

